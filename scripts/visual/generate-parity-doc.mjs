import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '../..');

const reportJsonPath = path.join(rootDir, 'artifacts/visual-diff-report.json');
const outputDocPath = path.join(rootDir, 'apps/storybook/src/docs/shared/ParityStatus.mdx');

function formatPercent(value) {
  if (!Number.isFinite(value)) return 'n/a';
  return `${value.toFixed(4)}%`;
}

function formatDate(iso) {
  if (!iso) return 'n/a';
  const date = new Date(iso);
  if (Number.isNaN(date.getTime())) return iso;
  return date.toISOString();
}

function summarizeByComponent(results) {
  const perComponent = new Map();

  for (const item of results) {
    const current = perComponent.get(item.component);
    const next = {
      component: item.component,
      status: item.status,
      worstDiffPercent: item.diffPercent,
      failedCases: item.status === 'FAIL' ? 1 : 0,
      totalCases: 1
    };

    if (!current) {
      perComponent.set(item.component, next);
      continue;
    }

    current.totalCases += 1;
    current.failedCases += next.failedCases;
    current.worstDiffPercent = Math.max(current.worstDiffPercent, next.worstDiffPercent);
    if (next.status === 'FAIL') current.status = 'FAIL';
  }

  return [...perComponent.values()].sort((a, b) => a.component.localeCompare(b.component));
}

async function generateFromReport(report) {
  const results = Array.isArray(report.results) ? report.results : [];
  const byComponent = summarizeByComponent(results);
  const threshold = Number.isFinite(report.thresholdPercent) ? report.thresholdPercent : 0.5;

  const tableRows = byComponent.length
    ? byComponent
        .map((item) => {
          const badge = item.status === 'PASS' ? '✅ PASS' : '❌ FAIL';
          return `| ${item.component} | ${badge} | ${formatPercent(item.worstDiffPercent)} | ${item.failedCases}/${item.totalCases} |`;
        })
        .join('\n')
    : '| _No results_ | - | - | - |';

  return [
    '# Visual Parity Status',
    '',
    `- Updated: **${formatDate(report.generatedAt)}**`,
    `- Threshold: **${threshold}% max diff per snapshot**`,
    `- Total Cases: **${report.total ?? 0}**`,
    `- Passed: **${report.passed ?? 0}**`,
    `- Failed: **${report.failed ?? 0}**`,
    '',
    '| Component | Status | Worst Diff | Failed Cases |',
    '| --- | --- | ---: | ---: |',
    tableRows,
    '',
    '> Source: `artifacts/visual-diff-report.json` (generated by `pnpm visual:report`).',
    ''
  ].join('\n');
}

async function generatePlaceholder() {
  return [
    '# Visual Parity Status',
    '',
    'No visual parity report found yet.',
    '',
    'Run:',
    '',
    '- `pnpm visual:baseline`',
    '- `pnpm visual:test`',
    '- `pnpm visual:report`',
    '',
    '> This page is generated by `scripts/visual/generate-parity-doc.mjs`.',
    ''
  ].join('\n');
}

async function main() {
  await mkdir(path.dirname(outputDocPath), { recursive: true });

  let content;
  try {
    const raw = await readFile(reportJsonPath, 'utf8');
    const report = JSON.parse(raw);
    content = await generateFromReport(report);
  } catch {
    content = await generatePlaceholder();
  }

  await writeFile(outputDocPath, content, 'utf8');
  console.log(`Wrote parity doc: ${outputDocPath}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
